#include "../includes/checker.h"

int		ft_nbrlen(int nb)
{
	int c;

	c = 1;
	if (nb == -2147483648)
		return (11);
	if (nb < 0)
	{
		nb = -nb;
		c++;
	}
	while (nb > 9)
	{
		nb = nb / 10;
		c++;
	}
	return (c);
}
